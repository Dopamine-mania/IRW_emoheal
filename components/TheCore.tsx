
import React, { useRef, useMemo } from 'react';
import { useFrame } from '@react-three/fiber';
import * as THREE from 'three';
import { useAudioAnalyzer } from '../hooks/useAudioAnalyzer';
import { useStore } from '../store';

const vertexShader = `
  uniform float uTime;
  uniform float uAudio;
  varying float vDisplacement;
  varying vec2 vUv;

  // Simplex noise (simplified)
  vec3 mod289(vec3 x) { return x - floor(x * (1.0 / 289.0)) * 289.0; }
  vec4 mod289(vec4 x) { return x - floor(x * (1.0 / 289.0)) * 289.0; }
  vec4 permute(vec4 x) { return mod289(((x*34.0)+1.0)*x); }
  vec4 taylorInvSqrt(vec4 r) { return 1.79284291400159 - 0.85373472095314 * r; }

  float snoise(vec3 v) {
    const vec2  C = vec2(1.0/6.0, 1.0/3.0) ;
    const vec4  D = vec4(0.0, 0.5, 1.0, 2.0);
    vec3 i  = floor(v + dot(v, C.yyy) );
    vec3 x0 = v - i + dot(i, C.xxx) ;
    vec3 g = step(x0.yzx, x0.xyz);
    vec3 l = 1.0 - g;
    vec3 i1 = min( g.xyz, l.zxy );
    vec3 i2 = max( g.xyz, l.zxy );
    vec3 x1 = x0 - i1 + C.xxx;
    vec3 x2 = x0 - i2 + C.yyy;
    vec3 x3 = x0 - D.yyy;
    i = mod289(i);
    vec4 p = permute( permute( permute(
              i.z + vec4(0.0, i1.z, i2.z, 1.0 ))
            + i.y + vec4(0.0, i1.y, i2.y, 1.0 ))
            + i.x + vec4(0.0, i1.x, i2.x, 1.0 ));
    float n_ = 0.142857142857;
    vec3  ns = n_ * D.wyz - D.xzx;
    vec4 j = p - 49.0 * floor(p * ns.z * ns.z);
    vec4 x_ = floor(j * ns.z);
    vec4 y_ = floor(j - 7.0 * x_ );
    vec4 x = x_ *ns.x + ns.yyyy;
    vec4 y = y_ *ns.x + ns.yyyy;
    vec4 h = 1.0 - abs(x) - abs(y);
    vec4 b0 = vec4( x.xy, y.xy );
    vec4 b1 = vec4( x.zw, y.zw );
    vec4 s0 = floor(b0)*2.0 + 1.0;
    vec4 s1 = floor(b1)*2.0 + 1.0;
    vec4 sh = -step(h, vec4(0.0));
    vec4 a0 = b0.xzyw + s0.xzyw*sh.xxyy ;
    vec4 a1 = b1.xzyw + s1.xzyw*sh.zzww ;
    vec3 p0 = vec3(a0.xy,h.x);
    vec3 p1 = vec3(a0.zw,h.y);
    vec3 p2 = vec3(a1.xy,h.z);
    vec3 p3 = vec3(a1.zw,h.w);
    vec4 norm = taylorInvSqrt(vec4(dot(p0,p0), dot(p1,p1), dot(p2, p2), dot(p3,p3)));
    p0 *= norm.x;
    p1 *= norm.y;
    p2 *= norm.z;
    p3 *= norm.w;
    vec4 m = max(0.6 - vec4(dot(x0,x0), dot(x1,x1), dot(x2,x2), dot(x3,x3)), 0.0);
    m = m * m;
    return 42.0 * dot( m*m, vec4( dot(p0,x0), dot(p1,x1), dot(p2,x2), dot(p3,x3) ) );
  }

  void main() {
    vUv = uv;
    
    // Base position
    vec3 pos = position;
    
    // Idle Breathing
    float breath = sin(uTime * 1.5) * 0.1;
    
    // Audio Displacement (Explosive)
    float noise = snoise(pos * 2.0 + uTime * 0.5);
    float displacement = breath + (noise * 0.3) + (uAudio * 1.2 * noise);
    
    vDisplacement = displacement;
    
    vec3 newPos = pos + normal * displacement;

    gl_Position = projectionMatrix * modelViewMatrix * vec4(newPos, 1.0);
    
    // Size attenuation
    gl_PointSize = (4.0 + uAudio * 10.0) * (1.0 / -vec4(modelViewMatrix * vec4(newPos, 1.0)).z);
  }
`;

const fragmentShader = `
  uniform float uTime;
  uniform float uAudio;
  varying float vDisplacement;
  
  void main() {
    // Create a circular particle
    float strength = distance(gl_PointCoord, vec2(0.5));
    strength = 1.0 - strength;
    strength = pow(strength, 3.0);
    
    // Base Color (Lavender/Purple)
    vec3 baseColor = vec3(0.6, 0.4, 0.9);
    
    // Active Color (Hot White/Cyan based on intensity)
    vec3 activeColor = vec3(0.8, 1.0, 1.0);
    
    // Mix based on displacement/audio
    vec3 finalColor = mix(baseColor, activeColor, uAudio + vDisplacement);
    
    // Alpha fade
    gl_FragColor = vec4(finalColor, strength);
  }
`;

export const TheCore: React.FC = () => {
  const meshRef = useRef<THREE.Points>(null);
  const materialRef = useRef<THREE.ShaderMaterial>(null);
  const { getAudioData } = useAudioAnalyzer();
  const phase = useStore(state => state.phase);

  const uniforms = useMemo(() => ({
    uTime: { value: 0 },
    uAudio: { value: 0 }
  }), []);

  useFrame((state) => {
    if (!materialRef.current || !meshRef.current) return;
    
    // Robust extraction of uniforms
    const uniforms = materialRef.current.uniforms;
    
    // Strictly check if uniforms and their properties exist
    if (!uniforms || !uniforms.uTime || !uniforms.uAudio) {
      return;
    }

    // Update time
    uniforms.uTime.value = state.clock.getElapsedTime();

    // Get Audio Data
    const { volume } = getAudioData();
    
    // Smooth lerp for audio visual
    // Check uAudio again just in case (defensive)
    if (uniforms.uAudio) {
      uniforms.uAudio.value = THREE.MathUtils.lerp(
        uniforms.uAudio.value,
        volume,
        0.1
      );
    }

    // Rotation
    meshRef.current.rotation.y += 0.002;
    meshRef.current.rotation.z += 0.001;
    
    // If resonance triggered, scale down quickly
    if (phase === 'resonance') {
        const currentScale = meshRef.current.scale.x;
        const targetScale = 0;
        const newScale = THREE.MathUtils.lerp(currentScale, targetScale, 0.1);
        meshRef.current.scale.setScalar(newScale);
    }
  });

  return (
    <points ref={meshRef}>
      <sphereGeometry args={[1.5, 64, 64]} />
      <shaderMaterial
        ref={materialRef}
        vertexShader={vertexShader}
        fragmentShader={fragmentShader}
        uniforms={uniforms}
        transparent
        blending={THREE.AdditiveBlending}
        depthWrite={false}
      />
    </points>
  );
};
